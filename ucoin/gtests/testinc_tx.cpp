    ASSERT_EQ(sizeof(SC1), tx.vin[0].script.len);
    ASSERT_EQ(0, memcmp(tx.vin[1].script.buf, SC2, sizeof(SC2)));
    ASSERT_EQ(sizeof(SC2), tx.vin[1].script.len);

    const uint8_t SCRIPTPK0[] = {
        12, 13, 14, 15, 16
    };
    const uint8_t SCRIPTPK1[] = {
        17
    };
    const ucoin_buf_t scriptpk0 = { (uint8_t *)SCRIPTPK0, sizeof(SCRIPTPK0) };
    const ucoin_buf_t scriptpk1 = { (uint8_t *)SCRIPTPK1, sizeof(SCRIPTPK1) };
    const ucoin_buf_t *pks[] = { &scriptpk0, &scriptpk1 };

    uint8_t sighash[UCOIN_SZ_SIGHASH];
